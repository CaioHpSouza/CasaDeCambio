public class ThreadSaque {
    
    private static void countMe(String name){
        int i = 0;
        i++;
        System.out.println("Current Counter is: " + i + ", updated by: " + name);
    }
    
    private static Runnable t1 = new Runnable() {
        public void run() {
            try{
                for(int i=0; i<5; i++){
                    countMe("t1");
                }
            } catch (Exception e){}

        }
    };

    private static Runnable t2 = new Runnable() {
        public void run() {
            try{
                for(int i=0; i<5; i++){
                    countMe("t2");
                }
            } catch (Exception e){}
       }
    };
}
